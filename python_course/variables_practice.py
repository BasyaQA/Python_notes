# переменная - это "контейнер" для хранения значения
# знак = означает присвоение

name = "Alice"
age = 25
print(name, age)

# or
name, age = "Alice", 25
print(name, age)

# поменять местами переменные
x = 3
y = 5

x, y = y, x
print(x, y)

# узнать тип значения переменной (int,str,float,bool etc.)
variable = 1
print(type(variable))
