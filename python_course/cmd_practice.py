# функция на вывод print()
print("Hello World")

# sep - разделитель
print("Hello", "World", sep=", ")

# end - аргумент для установки конечного символа (автоматом будет перенос на след строку)
print("first row", end=" ")
print("second row")

# функция на ввод input()
name = input("Please enter your name:")
print(name)
